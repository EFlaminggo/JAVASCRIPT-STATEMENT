// Contoh pengguanaan Switch Case
let Hewan = "burung";

switch (Hewan) {
  case "kucing":
    console.log("Ini adalah kucing.");
    break;
  case "anjing":
    console.log("Ini adalah anjing.");
    break;
  case "burung":
    console.log("Ini adalah burung.");
    break;
  default:
    console.log("Jenis hewan tidak dikenal.");
    break;
}
